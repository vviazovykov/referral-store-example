DROP TABLE IF EXISTS referee;

CREATE TABLE referee(
    id BIGINT NOT NULL AUTO_INCREMENT,
    user_id VARCHAR(255) NOT NULL,
    referral_code VARCHAR(255) NOT NULL,
    invitees_amount INTEGER,
    tier VARCHAR(15),
    currency VARCHAR(5),
    PRIMARY KEY (id),
    UNIQUE (id),
    UNIQUE (user_id)
);

